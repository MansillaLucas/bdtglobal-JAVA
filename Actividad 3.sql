SELECT
	emp.ID,
	emp.APELLIDO,
	emp.NOMBRE,
	(emp.APELLIDO + ' ' + emp.NOMBRE) AS NOMBRE_COMPLETO,
	emp.SALARIO,
	edu.DESCRIPCION
FROM TBL_empleados AS emp
INNER JOIN TBL_nivel_educacion AS edu
	ON emp.ID_EDUCACION = edu.ID
WHERE emp.ID_GERENCIA IN
	(SELECT
		g.ID
	 FROM 
		TBL_gerencias AS g
	 INNER JOIN
		TBL_empleados AS e
		ON g.ID = e.ID_GERENCIA
	 GROUP BY g.ID,
			  g.GERENCIA,
			  g.CIUDAD
	  HAVING SUM(e.SALARIO) >= 430000);

